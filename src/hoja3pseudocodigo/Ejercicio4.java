public class Ejercicio4 {
	public static void main(String[] args)
	{
	

		int suma= 0;
		
		System.out.println("La suma de los numeros del 1 al 100 es: " );	
		for (int i=1; i<=100; i++) {
			suma = suma + i;
			
		}
		System.out.print(suma);
	}

		
}

